def clean_and_update():
    with open(FILENAME, 'r', encoding='utf-8') as f:
        content = f.read()

    lines = content.splitlines()
    new_lines = []
    current_lang = None
    
    uo_prefixes = ["uomusume_", "maaji_", "madai_", "nijimasu_"]
    
    for line in lines:
        match = re.search(r'^\s*"([a-z-]+)":\s*\{', line)
        if match:
            current_lang = match.group(1)
            new_lines.append(line)
            if current_lang in uomusume_translations:
                for key, val in uomusume_translations[current_lang].items():
                    safe_val = val.replace('"', '\\"')
                    new_lines.append(f'    "{key}": "{safe_val}",')
            continue
        
        if line.strip() == '},' or line.strip() == '}':
            current_lang = None
            new_lines.append(line)
            continue
            
        if current_lang in uomusume_translations:
            is_uo_key = False
            for p in uo_prefixes:
                if f'"{p}' in line:
                    is_uo_key = True
                    break
            if is_uo_key:
                continue
        
        new_lines.append(line)

    with open(FILENAME, 'w', encoding='utf-8') as f:
        f.write("\n".join(new_lines))
    print("Successfully cleaned and updated i18n_indie.js with FINAL keys matching HTML")

if __name__ == "__main__":
    clean_and_update()
